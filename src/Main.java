public class Main {
    public static void main(String[] args) {
        double winterCost = 200.00; // Example winter maintenance cost
        double springCost = 150.00; // Example spring maintenance cost
        double summerCost = 100.00; // Example summer maintenance cost
        double fallCost = 180.00; // Example fall maintenance cost

        double totalYearlyCost = winterCost + springCost + summerCost + fallCost;

        System.out.println("Winter Cost: $" + winterCost);
        System.out.println("Spring Cost: $" + springCost);
        System.out.println("Summer Cost: $" + summerCost);
        System.out.println("Fall Cost: $" + fallCost);
        System.out.println("Total Yearly Maintenance Cost: $" + totalYearlyCost);
    }
}
